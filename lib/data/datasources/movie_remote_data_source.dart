Future<SecurityContext> get globalContext async {
  final sslCert = await rootBundle.load('certificates/themoviedb-org.pem');
  SecurityContext securityContext = SecurityContext(withTrustedRoots: false);
  securityContext.setTrustedCertificatesBytes(sslCert.buffer.asInt8List());
  return securityContext;
}

abstract class MovieRemoteDataSource {
  Future<List<MovieModel>> getNowPlayingMovies();
  Future<List<MovieModel>> getPopularMovies();
  Future<List<MovieModel>> getTopRatedMovies();
  Future<MovieDetailResponse> getMovieDetail(int id);
  Future<List<MovieModel>> getMovieRecommendations(int id);
  Future<List<MovieModel>> searchMovies(String query);
  Future<List<MovieModel>> getNowAiringShows();
  Future<List<MovieModel>> getPopularShows();
  Future<List<MovieModel>> getTopRatedShows();
  Future<ShowDetailResponse> getShowDetail(int id);
  Future<List<MovieModel>> getShowRecommendations(int id);
  Future<List<MovieModel>> searchShows(String query);
}

